function scaleHorizontally(sizeOneDigit, sizeToScale) {
    return sizeOneDigit.map(function(row) {
        var size = sizeToScale - 1;
        return(row.slice(0, 2)        
            .concat(row[1].repeat(size - 1).split(''))
            .concat(row.slice(2)));
    });
}

