function scaleHorizontally(sizeOneDigit, sizeToScale) {
    var scaled = sizeOneDigit.map(function(row){
        var size = sizeToScale-1;
        while( size > 0) {
            size --;
            row.splice(1, 0, row[1]);
        }
    });
    return scaled;
}

